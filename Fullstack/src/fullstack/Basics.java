package fullstack;

import java.util.Scanner;

public class Basics {
	public static void main(String[] args) {
		Scanner Sc = new Scanner(System.in);
		System.out.println("Enter The A Value");
		int a = Sc.nextInt();
		System.out.println("Enter The A Value");
		int b = Sc.nextInt();
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a|b);
		System.out.println(a%b);
}

}
	
	
